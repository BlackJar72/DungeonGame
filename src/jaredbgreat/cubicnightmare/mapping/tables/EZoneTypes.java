public enum EZoneTypes {
    
    LIVING,     // Living quarters
    MANOR,      // Nicer living quarters
    TEMPLE,     // Religious areas
    TOMB,       // Tombs, catacombs, crypts
    DUNGEON,    // Generic fantasy "dungeon"
    PRISON,     // Literal dungeon
    CAVES,      // Natural cave features (rocks, stalagtites/stalagmites)
    SHOPS,      // Vaious mechant areas (including bars/resturaunts)
    CRAFTING,   // Forges, tanning, carpentry
    CASTLE,     // Armories, guardrooms, etc., "stronghold", plus LIVING and MANOR areas
    ARCANE,     // School, lab, alchemical, and wizardly areas
    MINE,       // Mining areas
    OUTDOOR;    // Special, for areas with access to sky where bushes/trees might grow
    
    
}
